function titleFromFolder(name) {
  const base = String(name || '')
    .replace(/[-_]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
  return base.charAt(0).toUpperCase() + base.slice(1);
}

function buildBrochures() {
  // Load all images under 'education Booklets' recursively (case-insensitive extensions)
  const files = import.meta.glob('../content/education Booklets/**/*.{png,PNG,jpg,JPG,jpeg,JPEG,webp,WEBP,gif,GIF}', {
    eager: true,
    query: '?url',
    import: 'default'
  });

  // Optional per-booklet custom titles: place a title.txt or meta.json inside each booklet folder
  const titleTxtFiles = import.meta.glob('../content/education Booklets/**/title.txt', {
    eager: true,
    query: '?raw',
    import: 'default'
  });
  const metaJsonFiles = import.meta.glob('../content/education Booklets/**/meta.json', {
    eager: true
  });

  // Group by first-level folder after 'education Booklets/'
  const groups = {};
  const titlesMap = {};
  Object.entries(files).forEach(([path, url]) => {
    // Normalize and extract folder
    // Example path: '../content/education Booklets/Booklet1/B1IMG1.jpg'
    const parts = path.split('education Booklets/');
    if (parts.length < 2) return;
    const rest = parts[1];
    const segs = rest.split('/');
    // If an image is placed directly under the root (no subfolder), group it under 'General'
    const folder = segs.length >= 2 ? segs[0] : 'General';
    if (!groups[folder]) groups[folder] = [];
    groups[folder].push({ path, url });
  });

  // Build titles map from title.txt
  Object.entries(titleTxtFiles).forEach(([path, raw]) => {
    const parts = path.split('education Booklets/');
    if (parts.length < 2) return;
    const rest = parts[1];
    const segs = rest.split('/');
    if (segs.length < 2) return;
    const folder = segs[0];
    const text = String(raw || '').trim();
    if (text) titlesMap[folder] = text;
  });

  // Build titles map from meta.json (expects { "title": "..." })
  Object.entries(metaJsonFiles).forEach(([path, mod]) => {
    const parts = path.split('education Booklets/');
    if (parts.length < 2) return;
    const rest = parts[1];
    const segs = rest.split('/');
    if (segs.length < 2) return;
    const folder = segs[0];
    try {
      const data = mod && (mod.default ?? mod);
      const t = data && (data.title ?? data.name);
      if (t && String(t).trim()) titlesMap[folder] = String(t).trim();
    } catch (_) {
      /* ignore malformed json */
    }
  });

  // Build brochures array
  const brochures = Object.entries(groups).map(([folder, items], idx) => {
    // Natural sort pages by filename to keep a stable order (e.g., IMG2 before IMG10)
    const collator = new Intl.Collator(undefined, { numeric: true, sensitivity: 'base' });
    const sorted = items
      .slice()
      .sort((a, b) => collator.compare(a.path, b.path))
      .map((it) => it.url);

    return {
      id: `brochure-${idx + 1}`,
      title: titlesMap[folder] || titleFromFolder(folder),
      pages: sorted
    };
  });

  // Stable overall order by folder name
  brochures.sort((a, b) => a.title.localeCompare(b.title));
  return brochures;
}

export const EDUCATION_BROCHURES = buildBrochures();

export function getBrochureById(id) {
  return (EDUCATION_BROCHURES || []).find((b) => String(b.id) === String(id)) || null;
}
