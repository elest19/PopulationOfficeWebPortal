function resolveFromMap(filesMap, filename) {
  if (!filename) return '';
  const target = String(filename).trim().toLowerCase();
  // Prefer exact filename match
  for (const [path, url] of Object.entries(filesMap)) {
    const name = path.split('/').pop().toLowerCase();
    if (name === target) return url;
  }
  // Fallback: endsWith for safety
  for (const [path, url] of Object.entries(filesMap)) {
    if (path.toLowerCase().endsWith('/' + target)) return url;
  }
  return '';
}

export function resolveEducationWebThumbnail(filename) {
  return resolveFromMap(EDUCATION_WEB_THUMBNAILS, filename);
}

export function resolveEducationWebDiagram(filename) {
  return resolveFromMap(EDUCATION_WEB_DIAGRAMS, filename);
}

// Generic resolver (tries thumbnail then diagram). Useful as a fallback.
export function resolveEducationWebAsset(filename) {
  return resolveEducationWebThumbnail(filename) || resolveEducationWebDiagram(filename) || '';
}

